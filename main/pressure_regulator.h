#pragma once

#include <stdbool.h>
#include <stdint.h>

// ============================================================================
//  КАСКАДНЫЙ РЕГУЛЯТОР ДАВЛЕНИЯ (rate-control + тонкая I-доводка)
// ----------------------------------------------------------------------------
//  Идея в двух словах:
//
//    ВНЕШНИЙ слой  : ошибка по давлению -> ЖЕЛАЕМАЯ СКОРОСТЬ dP/dt (таблица).
//                    Далеко от цели хотим быстро, близко — медленно.
//                    Это, по сути, профиль приближения ("умная рампа").
//
//    ВНУТРЕННИЙ PI : держит РЕАЛЬНУЮ скорость dP/dt равной желаемой,
//                    выход = открытие иглы (шаги). Коэффициенты ФИКСИРОВАННЫЕ
//                    (настраиваются один раз) — связь "игла->расход->скорость"
//                    почти линейна по всему диапазону, поэтому адаптировать
//                    коэффициенты не нужно: нелинейность объекта (расход слабеет
//                    у цели) гасится самой обратной связью по скорости.
//
//    У ЦЕЛИ        : фаза HOLD (с |error| <= hold_enter_err PID выключен совсем).
//                    В покое объём ЗАПЕЧАТАН: игла на valve_flow_floor (ниже
//                    порога потока), серво в нейтрали — газ из трубки серво->игла
//                    не сочится в объём (подтверждённая причина ползучести).
//                    Коррекции — МИКРОДОЗЫ: серво в нужную сторону, игла на
//                    фиксированном дозирующем открытии step_holding_charge/vent;
//                    раз в 5 с открытие подстраивается по ФАКТИЧЕСКОМУ прогрессу
//                    давления за окно (а не по dP/dt — у цели нужные 0.01-0.02
//                    кПа/с тонут в шуме измерения скорости). Ошибка дошла до
//                    порога печати — игла на парковку, серво в нейтраль.
//
//    ТОЧНЫЙ ХОЛДИНГ : (вкл. конфигом hold_fine_enable) как только НАБОРОМ вышли
//                    на точку и доза подобрана делом — объём НЕ печатаем: серво
//                    остаётся в НАБОРЕ, игла на step_holding_charge-fine_seed_back,
//                    дальше раз в fine_period_us +-fine_trim по знаку скорости
//                    (росло давление за окно -> прикрыть, падало -> приоткрыть).
//                    Игла ищет приоткрытие, где приток равен утечке, и стоит на
//                    нём БЕЗ периодических подкачек. Смещение от цели (просадка
//                    за время поиска и т.п.) лечится ВРЕМЕННОЙ КОРРЕКЦИЕЙ: окно
//                    показало «стоим» (|dP| < fine_eq_band), но дальше
//                    fine_corr_err от цели -> игла на fine_corr_step от
//                    равновесия в нужную сторону (само равновесие не трогаем);
//                    РОВНО дошли до цели -> игла обратно на равновесие. Запекать
//                    равновесие через печать/докачку нельзя — пока объём запечатан,
//                    магистраль успевает измениться и значение протухает
//                    (проверено: найденные 3393 после докачки уже лили).
//                    Выход на точку СБРОСОМ сюда не ведёт: печатаем, ждём
//                    естественного стравливания вниз, эпизод НАБОРА подбирает
//                    дозу и выводит на точку — и только тогда включается точный
//                    холдинг. Увод дальше hold_enter_err — возврат в обычный
//                    HOLD (эпизоды); повторный вход FINE стартует с найденного
//                    равновесия и дальше подстраивает его окнами как обычно.
//
//  Направление (накачка/сброс) задаёт серво (180/0 град), величину потока —
//  игла (шаговик). Поэтому открытие иглы всегда >= 0, а знак берёт на себя серво.
// ============================================================================

// Положение 3-позиционного серво-крана направления.
typedef enum {
    SERVO_NEUTRAL,   // 90 град — объём изолирован (держим)
    SERVO_CHARGING,  // 180 град — подаём давление
    SERVO_VENTING    // 0 град — стравливаем
} ServoState;

// Состояния автомата регулятора. Снаружи используется только REG_STATE_IDLE.
typedef enum {
    REG_STATE_NONE = 0, // «нет запроса» — служебное значение для requested_reg_state
    REG_STATE_IDLE,     // ничего не делаем, серво в нейтрали, игла закрыта
    REG_STATE_HOMING,   // принудительный физический сброс давления
    REG_STATE_RUNNING   // активное регулирование (внутри: дальняя/тонкая зона)
} RegulatorState;

typedef struct {
    // ---- общее ----
    RegulatorState state;
    ServoState     servo_state;     // текущее положение серво (чтобы дёргать его только при смене)
    float          dt;              // реальный шаг цикла, сек (меряется по таймеру)
    float          active_setpoint; // уставка, на которой сейчас работаем (для детекта смены цели)

    // ---- измерение скорости (производная давления) ----
    float    prev_pressure;         // давление на прошлом СВЕЖЕМ отсчёте датчика
    uint64_t last_pressure_us;      // время прошлого свежего отсчёта (мкс) — для честного dP/dt
    float    raw_rate;              // сырая dP/dt (без фильтра) — только для лога/сравнения
    float    filtered_rate;         // отфильтрованная dP/dt, кПа/с
    float    rate_filter_alpha;     // коэф. фильтра скорости (0..1, ближе к 1 = сильнее сглаживание)
    float    filtered_pressure;     // отфильтрованное давление (EMA) — порог HOLD считаем по нему
    float    press_filter_alpha;    // коэф. фильтра давления (0..1, ближе к 1 = сильнее сглаживание)
    uint16_t glitch_skips;          // подряд отброшенных «битых» отсчётов датчика (анти-залипание)

    // ---- ВНУТРЕННИЙ PI по скорости (дальняя зона) ----
    float   rate_kp;                // реакция на отклонение скорости (шаги на кПа/с)
    float   rate_ki;               // набор стационарного открытия (шаги на кПа/с * с)
    float   rate_integral;          // интегратор внутреннего контура (только >= 0)
    float   rate_integral_max;      // ограничение интегратора (anti-windup)

    // ---- порог шума датчика / ползучая скорость у цели ----
    float sensor_noise_delta;       // макс. дрожание датчика в покое (кПа). Двойная роль:
                                    //  - порог ВЫХОДА из холда (|error| больше -> снова регулируем);
                                    //  - в полосе error 0..2 = желаемая скорость подхода к цели.
    float sensor_noise_delta_filt; // а это тоже самое но с учетом фильтра 80%-20%

    // ---- состояние холда: в покое ЗАПЕЧАТАНО (игла на valve_flow_floor, серво
    //      нейтраль), коррекция = серво задаёт направление + игла на фиксированном
    //      дозирующем открытии (МИКРОДОЗА); открытие подстраивается раз в окно по
    //      фактическому прогрессу давления, без PI ----
    bool     holding;               // true = фаза HOLD (PID выключен до новой уставки)
    float    hold_enter_err;        // |error| <= этого -> RATE выключается, входим в HOLD
    int32_t  dose_step_back;        // вход в HOLD: стартовое открытие = позиция RATE минус это
    int32_t  step_holding_charge;   // дозирующее открытие иглы для НАБОРА (живёт между эпизодами)
    int32_t  step_holding_vent;     // то же для СБРОСА (перепад на игле другой — своё значение)
    bool     dose_charge_calibrated; // false: доза НАБОРА ещё ни разу не дала потока (засеяна floor'ом)
                                     // -> в ветке «медленно» прибавляем dose_trim_big, а не dose_trim
    bool     dose_vent_calibrated;   // то же для СБРОСА
    uint64_t dose_t0_us;            // начало текущего окна проверки прогресса (мкс)
    float    dose_p0;               // P_filt в начале окна
    uint64_t dose_period_us;        // длительность окна проверки (5 с)
    float    dose_dp_slow;          // прогресс за окно МЕНЬШЕ -> приоткрыть на dose_trim
    float    dose_dp_VERYslow;      // прогресс за окно В ОБРАТНУЮ СТОРОНУ -> открыть сильно на dose_trim_big
    float    dose_dp_fast;          // прогресс БОЛЬШЕ -> прикрыть на dose_trim
    float    dose_dp_runaway;       // прогресс СИЛЬНО больше -> прикрыть на dose_trim_big
    int32_t  dose_trim;             // мелкий шаг подстройки открытия (шаги иглы)
    int32_t  dose_trim_big;         // грубый шаг подстройки при разгоне
    int32_t  dose_trim_very_big;         // очень грубый шаг подстройки до калиброки угла на точке

    // ---- ВТОРОЙ (ТОЧНЫЙ) ХОЛДИНГ: равновесное приоткрытие вместо подкачек ----
    bool     hold_fine_enable;      // КОНФИГ: true = после выхода на точку НАБОРОМ переходить в
                                    // точный холдинг; false = старое поведение (печать + эпизоды)
    bool     fine_holding;          // true = сейчас точный холдинг: серво стоит в НАБОРЕ,
                                    // игла на равновесном приоткрытии
    int32_t  fine_pos;              // текущее открытие иглы точного холдинга
    int32_t  fine_seed_back;        // вход БЕЗ найденного равновесия: step_holding_charge минус это
    int32_t  fine_trim;             // подстройка открытия за окно (+- по знаку скорости)
    uint64_t fine_period_us;        // окно оценки знака скорости (5 с)
    float    fine_eq_band;          // |dP| за окно МЕНЬШЕ этого = «стоим»: равновесие найдено
    bool     fine_eq_found;         // равновесное открытие найдено и запомнено в fine_eq_pos
    int32_t  fine_eq_pos;           // найденное равновесное открытие: игла стоит на нём,
                                    // повторный вход FINE стартует сразу с него
    int32_t  fine_corr_step;        // временная коррекция: отступ иглы от равновесия (шаги)
    float    fine_corr_err;         // «стоим», но |err_filt| больше этого -> запускаем коррекцию
    int32_t  fine_corr_dir;         // 0 = коррекции нет; +1/-1 = игла на fine_eq_pos +/- corr_step,
                                    // err_filt пересёк ноль -> игла обратно на fine_eq_pos
    int32_t  fine_trim_fast;        // крупная подстройка за окно, пока давление ещё активно
                                    // движется (fine_change >= fine_hold_change) — вместо fine_trim
    float    fine_change;           // |ΔP_filt| за последние ~5 с (скользящее окно) — гейт FINE->HOLD
    float    fine_hold_change;      // fine_change >= этого = «ещё движусь»: остаёмся в FINE,
                                    // крупный шаг, в HOLD не уходим (защита от дёрганья у порога)
    uint64_t fine_rate_t0_us;       // трекер fine_change: начало текущего чекпойнта (мкс)
    float    fine_rate_p_cur;       // P_filt на последнем чекпойнте (0..period/2 назад)
    float    fine_rate_p_prev;      // P_filt на предыдущем чекпойнте (period/2..period назад)

    // ---- лимиты привода ----
    int32_t valve_max;              // макс. открытие иглы (шаги)
    int32_t valve_flow_floor;       // открытие, ниже которого игла НЕ пропускает газ (мёртвый ход)
    int32_t max_step;               // макс. сдвиг иглы за один тик (плавность/защита)
} PressureRegulator;

// Запрос состояния извне (например команда idle/abort -> REG_STATE_IDLE).
extern volatile RegulatorState requested_reg_state;
void regulator_request_state(RegulatorState new_state);

// Инициализация регулятора фиксированными коэффициентами.
void regulator_init(PressureRegulator* reg, float dt);

// Главная задача регулятора (создаётся в regulator_start_task).
void pid_regulator_task(void *pvParameters);
void regulator_start_task(void);
